function get_gitsrc(uri, repodir)
  if isdir(repodir)
    gitrepo = LibGit2.GitRepo(repodir)
  else
    gitrepo = LibGit2.clone(uri, repodir)
  end
  ref   = LibGit2.head(gitrepo)
  hash  = LibGit2.GitHash(ref)
  lhash = string(hash)
  shash = string(LibGit2.GitShortHash(hash, 7))

  return (lhash, shash)
end

function ff(x, y)
